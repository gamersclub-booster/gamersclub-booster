export function getMapImage( map ) {
  switch ( map ) {
  case 'de_mirage':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/posi%C3%A7%C3%B5es-csgo-mirage.jpg';

  case 'de_dust2':
    return 'https://i.pinimg.com/originals/78/56/79/785679110c70f573dbe130420faea69a.jpg';

  case 'de_nuke':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/posi%C3%A7%C3%B5es-csgo-nuke-768x627.jpg?ezimgfmt=ng:webp/ngcb2';

  case 'de_train':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/posi%C3%A7%C3%B5es-csgo-train.jpg?ezimgfmt=ng:webp/ngcb2';

  case 'de_overpass':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/posi%C3%A7%C3%B5e-csgo-overpass.jpg?ezimgfmt=ng:webp/ngcb2';

  case 'de_inferno':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/posi%C3%A7%C3%B5es-csgo-inferno.jpg?ezimgfmt=ng:webp/ngcb2';

  case 'de_vertigo':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/vertigo-posicoes-0-cke.jpg?ezimgfmt=ng:webp/ngcb2';

  case 'de_cbble_classic':
    return 'https://www.esportelandia.com.br/wp-content/uploads/2020/01/posi%C3%A7%C3%B5es-csgo-cobblestone.jpg?ezimgfmt=ng:webp/ngcb2';

  case 'de_ancient':
    return 'https://pbs.twimg.com/media/E0joah7WQAALf5n?format=png&name=large';

  default:
    return '';
  }
}
